    public void delete() {
        dc.setMaDiaChi(dc.getMaDiaChi());
        dc.setTenDN(getTenDN());
        int x = dcDao.Delete(dc);
        if (x > 0) {
            Container container = this.getParent();
            container.remove(this);
            container.revalidate();
            container.repaint();
            JOptionPane.showMessageDialog(this, "Xóa dịa chỉ thành công!");
        } else {
            JOptionPane.showMessageDialog(this, "Xóa dịa chỉ thất bại!");
        }
    }
