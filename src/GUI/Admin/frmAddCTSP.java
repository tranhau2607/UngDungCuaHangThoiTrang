    public void addCTSP() {

        ctsp.setMaSanPham(txtMaSP.getText());
        ctsp.setThuongHieu(txtThuongHieu.getText());
        ctsp.setMaCT(txtCTSP.getText());
        ctsp.setMoTa1(txtMoTa1.getText());
        ctsp.setMoTa2(txtMoTa2.getText());
        ctsp.setMoTa3(txtMoTa3.getText());
        ctsp.setMoTa4(txtMoTa4.getText());
        ctsp.setTenSanPham(getTENSP());
        ctsp.setGia(getGIA());
        ctsp.setAnh1(lblTenHinh1.getText());
        ctsp.setAnh2(lblTenHinh2.getText());
        ctsp.setAnh3(lblTenHinh3.getText());
        ctsp.setIsDelete(0);

        int x = ctspDao.Save(ctsp);
        if (x > 0) {
            JOptionPane.showMessageDialog(this, "Thêm chi tiết sản phẩm thành công!");
            this.dispose();
        } else {
            JOptionPane.showMessageDialog(this, "Thêm chi tiết sản phẩm thất bại!");
        }

    }

    public String ChonHinh() {
        JFileChooser fileChooser = new JFileChooser();
        String defaultPath = "src\\IMG_Product\\IMG_Product23";
        fileChooser.setCurrentDirectory(new File(defaultPath));
        FileNameExtensionFilter filter = new FileNameExtensionFilter("Images", "jpg", "png", "gif", "jpeg");
        fileChooser.setFileFilter(filter);

        int returnValue = fileChooser.showOpenDialog(null);

        if (returnValue == JFileChooser.APPROVE_OPTION) {
            File selectedFile = fileChooser.getSelectedFile();

            String filename = selectedFile.getName();
            return filename;
        }
        return null;
    }

