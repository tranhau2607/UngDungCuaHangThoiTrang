    public void addNSX() {
        nsx.setMaNhaSanXuat(txtMaNSX.getText());
        nsx.setTenNhaSanXuat(txtTenNSX.getText());
        nsx.setDiaChi(txtDiaChi.getText());
        nsx.setSoDienThoai(txtSDT.getText());
        nsx.setEmail(txtEmail.getText());

        int x = nsxDao.Save(nsx);
        if (x > 0) {
            JOptionPane.showMessageDialog(this, "Thêm nhà sản xuất thành công!");
            this.dispose();
            loadForm.fillToTable();
        } else {
            JOptionPane.showMessageDialog(this, "Thêm nhà sản xuất thất bại!");
        }
    }

    private void ktMa(StringBuilder sb2) {
        if (nsxDao.ktTrungMa(txtMaNSX.getText()) != 0) {
            sb2.append("Mã nhà sản xuất bạn nhập đã có!\n");
        }
    }

    private void ktRong(StringBuilder sb) {

        if (txtMaNSX.getText().equals("")) {
            sb.append("Mã nhà sản xuất không được để trống\n");
        }
        if (txtTenNSX.getText().equals("")) {
            sb.append("Tên nhà sản xuất không được để trống\n");
        }
        if (txtDiaChi.getText().equals("")) {
            sb.append("Địa chỉ không được để trống\n");
        }
        if (txtEmail.getText().equals("")) {
            sb.append("Email không được để trống\n");
        }

        if (txtSDT.getText().equals("")) {
            sb.append("Số điện thoại không được để trống\n");
        } else {
            String soDT = txtSDT.getText();
            if (StringUtils.isEmpty(soDT) || !soDT.matches("[0-9]{10}")) {
                sb.append("Số điện thoại không hợp lệ (phải là 10 chữ số)\n");
            }
        }
    }

    private void ktTrungEmail(StringBuilder sb1) {
        if (nsxDao.ktTrungEmail(txtEmail.getText()) != 0) {
            sb1.append("Email đã đã trùng với nhà sản xuất khác!\n Vui lòng nhập Enail khác");
        }
    }

    private boolean dinhDangEmail(String email) {
        String dinhdang = "^[a-zA-Z0-9_+&*-]+(?:\\.[a-zA-Z0-9_+&*-]+)*@(?:[a-zA-Z0-9-]+\\.)+[a-zA-Z]{2,7}$";
        return email.matches(dinhdang);
    }
