    public void addLSP() {
        lsp.setMaLoai(txtMaLSP.getText());
        lsp.setTenLoai(txtTenLSP.getText());

        int x = lspDao.Save(lsp);
        if (x > 0) {
            JOptionPane.showMessageDialog(this, "Thêm loại sản phẩm thành công!");
            this.dispose();
            if (loadForm != null) {
                loadForm.fillToTable();
            }
        } else {
            JOptionPane.showMessageDialog(this, "Thêm loại sản phẩm thất bại!");
        }

    }

    private void ktMa(StringBuilder sb2) {
        if (lspDao.ktTrungMa(txtMaLSP.getText()) != 0) {
            sb2.append("Mã loại bạn nhập đã có!\n");
        }
    }

    private void ktRong(StringBuilder sb) {

        if (txtMaLSP.getText().equals("")) {
            sb.append("Mã loại không được để trống\n");
        }
        if (txtTenLSP.getText().equals("")) {
            sb.append("Tên loại không được để trống\n");
        }

    }

